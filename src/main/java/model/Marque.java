package model;

public class Marque {
    Integer id_marque;
    String libelle;
    
    public Integer getId_marque() {
        return id_marque;
    }
    public void setId_marque(Integer id_marque) {
        this.id_marque = id_marque;
    }
    public String getLibelle() {
        return libelle;
    }
    public void setLibelle(String libelle) {
        this.libelle = libelle;
    }
}
